export type UiLanguage = 'ru' | 'en' | 'tg';

export const languageLabel: Record<UiLanguage, string> = {
  ru: 'Русский',
  en: 'English',
  tg: 'Тоҷикӣ',
};

export function guessLanguageFromLocale(locale: string | undefined): UiLanguage {
  const l = (locale ?? '').toLowerCase();
  if (l.startsWith('ru')) return 'ru';
  if (l.startsWith('tg')) return 'tg';
  return 'en';
}

export const STORAGE_KEY = 'lmnt_language';
export const COOKIE_KEY = 'lmnt_lang';

export function readStoredLanguage(): UiLanguage | null {
  if (typeof window === 'undefined') return null;
  const v = window.localStorage.getItem(STORAGE_KEY);
  return v === 'ru' || v === 'en' || v === 'tg' ? v : null;
}

export function writeStoredLanguage(lang: UiLanguage) {
  if (typeof window === 'undefined') return;
  window.localStorage.setItem(STORAGE_KEY, lang);
}

export function parseUiLanguage(value: string | undefined | null): UiLanguage | null {
  if (value === 'ru' || value === 'en' || value === 'tg') return value;
  return null;
}

